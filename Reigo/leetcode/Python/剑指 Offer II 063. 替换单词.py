from typing import List
class TrieNode:
    def __init__(self):
        self.next={}
        self.word=None



class Solution:
    def replaceWords(self, dictionary: List[str], sentence: str) -> str:
        root=TrieNode()
        for word in dictionary:
            node=root
            for l in word:
                if l not in node.next:
                    node.next[l]=TrieNode()
                node=node.next[l]
            node.word=word
        
        sl=sentence.split(" ")
        for i,word in enumerate(sl):
            node=root
            for l in word:
                if l not in node.next:
                    break
                node=node.next[l]
                if node.word:
                    break
            #print(node.word)
            if node.word:
                sl[i]=node.word
        print(sl)
        #return "abcderfg"
        return " ".join(sl)

